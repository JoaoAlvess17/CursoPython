mood = input("Qual é o teu estado de espírito hoje? ")

if mood == "Feliz":
    print("É a Melhor maneira de estar.")
elif mood == "Triste":
    print("Vais ter melhores dias certamente.")
elif mood == "Nervoso":
    print("Bebe uma cerveja que ajuda a relaxar.")
elif mood == "Ansioso":
    print("Respira fundo e acalma-te.")
elif mood == "Relaxado":
    print("Cuidado, não fiques relaxado de mais.")
else:
    print("Não reconheço esse estado de espírito.")